<h4>Making bonds that last</h4>

<p>Biotron Laboratories was founded in 1979 by esteemed and highly accomplished scientist Dr. M.Taher Fouad. A dedicated and enthusiastic student, Fouad received a M.Sc. in Microbiology while working at the University of Reading in England, and completed his Ph.D. in Virology at Cornell University. Following completion of post-doctoral training at the University of Kansas Medical Center, he worked in the biological and pharmaceutical industries, successfully developing a parainfluenza vaccine&ndash;a vaccine in which the U.S. Patent Office named him the sole inventor.</p>

<p>It was during this time that Dr. Fouad discovered his true passion, and began to focus his energies on the role of minerals and trace elements in a vigorous immune system, disease prevention and ultimately, general health maintenance. Dr. Fouad began exploring the potential of chelation and how a properly prepared mineral chelate could increase the nutritional value of essential minerals and trace elements. In 1971 he developed a unique process for preparing these compounds, which led to the extensive use of chelated minerals in human nutrition. </p> 

<p>Dr. Fouad's considerable interest and focus in nutrition, coupled with his industrial background culminated in the establishment of Biotron Laboratories, Inc in 1979, where he remained active well into his 80's. Today Biotron continues Dr. Fouad's life mission with an ever-growing commitment to discovering and developing mineral material products that promote the development of a healthful body.</p> 
