	<h2>Study Design</h2>
	
	<p>To demonstrate superior bioavailabilty of Cytocal&trade; relative to inorganic calcium (as calcium carbonate), a single center, randomized, double blind, acute crossover pilot study was performed to test the pharmacokinetics of absorption and effects on parathyroid hormone levels in healthy women.</p>

<p>The acute study consisted of two 12 hour monitored treatment periods with a minimum one-week washout period. A low calcium diet was followed for seven days prior to each test.  Four calcium tablets (500 mg total Ca) were consumed in clinic after which blood was drawn at 30 minutes, as well as 2,4,6,9 and 12 hours post-dose.  A pre-dose sample was collected to determine baseline values. Fifteen volunteers were enrolled of which 14 completed the study. The pharmacokinetics and relative bioavailability were measured (Cmax, AUC (0-12h) and Tmax). Levels of parathyroid hormone, which decrease in response to increasing serum calcium concentration, were also evaluated. The safety of the two test materials were carefully monitored throughout the course of the investigation.</p>
	
